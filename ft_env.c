#include "minishell.h"

void	ft_env(char **env)
{
	int		i;

	if (!env)
		return ;
	i = 0;
	while (env[i])
	{
		if (env[i][0] != '\0')
		{
			ft_putstr(env[i]);
			ft_putchar('\n');
		}
		i++;
	}
}
